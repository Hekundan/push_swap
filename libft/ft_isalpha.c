int	ft_isalpha(int c)
{
	unsigned char	uc;

	if (c < 0 || c > 255)
		return (0);
	uc = (unsigned char) c;
	if (((uc >= 'a') && (uc <= 'z')) || ((uc >= 'A') && (uc <= 'Z')))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
