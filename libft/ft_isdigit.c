int	ft_isdigit(int c)
{
	unsigned char	uc;

	if (c < 0 || c > 255)
		return (0);
	uc = (unsigned char) c;
	if ((uc >= '0' && uc <= '9'))
		return (1);
	else
		return (0);
}
